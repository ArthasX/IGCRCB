CREATE OR REPLACE FUNCTION IG.TRIG_005_F() 
	RETURNS TRIGGER AS $$
DECLARE
  FLAG INT := 0;
BEGIN
  IF NEW.PIDMODE = '0' THEN
    SELECT COUNT(1)
      INTO FLAG
      FROM IG.IG500 IG500
     INNER JOIN IG.IG380 B ON (IG500.PRPDID = B.PDID)
     WHERE B.PDSTATUS = 'a'
       AND IG500.PRID = NEW.PRID;
    IF FLAG = 1 THEN
      INSERT INTO IG.R02
        (R0201, R0202, R0203, R0204, R0205, R0206, R0207, R0208, R0209)
      VALUES
        (NEW.PIID,
         NEW.PRID,
         NEW.PIVARNAME,
         NEW.PIVARLABEL,
         NEW.PIVARVALUE,
         NEW.PIDMODE,
         0,
         NULL,
         NULL);
    END IF;
  END IF;
   RETURN NEW;
END;
$$
LANGUAGE plpgsql;

