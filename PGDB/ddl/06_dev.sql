       R0121,
       R0122,
       R0123,
       R0124,
       R0125,
       R0126,
       R0127,
       R0128,
       R0129,
       R0130,
       R0131)
      SELECT NEW.PPID,
             IG500.PRID,
             IG500.PRTYPE,
             IG500.PRSTATUS,
             IG500.PRTITLE,
             IG500.PRDESC,
             IG500.PROPENTIME,
             IG500.PRCLOSETIME,
             IG500.PRPDID,
             IG500.PRPDNAME,
             IG500.PRSERIALNUM,
             NEW.PPROLEID,
             NEW.PPROLENAME,
             NEW.PPUSERID,
             NEW.PPUSERNAME,
             NEW.PPORGID,
             (SELECT ORGNAME FROM IG.ORGANIZATION WHERE CAST(ORGID AS VARCHAR) = NEW.PPORGID),
             A.PSDNAME,
             B.PSDNAME,
             NEW.PPSTATUS,
             NEW.PPINITTIME,
             NEW.PPPROCTIME,
             NEW.PPSUBSTATUS,
             NEW.PPBACKTIME,
             NEW.PPPROXYUSERID,
             NEW.PPSUPER,
             NEW.PPROLEMANAGER,
             NEW.PPDUTYPERSON,
             NEW.PBDID,
             (SELECT USERNAME FROM IG.IGUSER WHERE USERID = NEW.PPPROXYUSERID),
             C.PTNAME
       FROM IG.IG500 IG500
       INNER JOIN IG.IG173 A ON (IG500.PRPDID = A.PDID AND IG500.PRSTATUS = A.PSDCODE)
       INNER JOIN IG.IG173 B ON (IG500.PRPDID = B.PDID AND B.PSDCODE = NEW.PPSTATUS)
       INNER JOIN IG.IG259 C ON (IG500.PRTYPE = C.PTTYPE)
       WHERE PRID = NEW.PRID;
  END IF;
  RETURN NEW;
END;
$$
LANGUAGE plpgsql;

