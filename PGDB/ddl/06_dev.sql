             A.PRID,
             A.PIVARNAME,
             A.PIVARLABEL,
             NEW.PPIVALUE,
             A.PIDMODE,
             NEW.PPIID,
             NEW.ROLEID,
             NEW.USERID,
             (SELECT ROLENAME FROM IG.ROLE WHERE ROLEID = NEW.ROLEID),
             (SELECT USERNAME FROM IG.IGUSER WHERE USERID = NEW.USERID)
        FROM IG.IG599 A
       WHERE A.PIID = NEW.PIID;
  END IF;
RETURN NEW;
END;
$$
LANGUAGE plpgsql;


