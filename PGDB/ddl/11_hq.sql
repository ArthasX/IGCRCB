--1. 警报列表信息视图 UPDATE DATE 2012/11/07
CREATE OR REPLACE VIEW HQ_ALERT_LIST_VIEW AS
SELECT
       A.ID,A.CTIME,A.FIXED,A.ALERT_DEFINITION_ID,
       B.NAME AS DEFNAME,B.ACTIVE,B.DELETED,B.PRIORITY,RESOURCE_ID,B.ENABLED,
       C.NAME AS CONDNAME,
       C.VERSION_COL,C.TYPE,C.OPTION_STATUS,
       C.COMPARATOR,C.THRESHOLD,
       D.VALUE
FROM
       EAM_ALERT A,
       EAM_ALERT_DEFINITION B,
       (SELECT ID,ALERT_DEFINITION_ID,VERSION_COL,TYPE,OPTION_STATUS,COMPARATOR,THRESHOLD,ARRAY_TO_STRING(ARRAY(SELECT NAME FROM EAM_ALERT_CONDITION C1 WHERE C1.ALERT_DEFINITION_ID = C2.ALERT_DEFINITION_ID ORDER BY ID), ' AND ' ) AS NAME 
       FROM EAM_ALERT_CONDITION C2 GROUP BY ID,ALERT_DEFINITION_ID ORDER BY ID) C,
       (SELECT CONDITION_ID,ALERT_ID,ARRAY_TO_STRING(ARRAY(SELECT VALUE FROM EAM_ALERT_CONDITION_LOG L1 WHERE L1.ALERT_ID = L2.ALERT_ID ORDER BY ID), '#' ) AS VALUE 
       FROM EAM_ALERT_CONDITION_LOG L2 GROUP BY CONDITION_ID,ALERT_ID) D
WHERE
       A.ALERT_DEFINITION_ID = B.ID
       AND B.ID = C.ALERT_DEFINITION_ID
       AND A.ID = D.ALERT_ID;

