 ------------------------------------------------短信平台触发器 UPDATE DATE 2012/11/12---------------------------

 CREATE OR REPLACE FUNCTION INSERT_SMS_QUEUE_F() 
 	RETURNS TRIGGER AS $$
 DECLARE
  STATUS DECIMAL;
  BEGIN
  SELECT CVALUE INTO STATUS FROM IG.CODEDETAIL WHERE CCID = '153' AND CID = '5';
  IF STATUS = 1 THEN
  INSERT INTO SMS_QUEUE (QID,ALERTID,TEL,USERID,USERNAME,SMSCONTEXT,ALERTTIME)
  SELECT nextval('SMS_QUEUE_SEQ'),
       NEW.ID,
       F.USERMOBILE AS TEL,
       F.USERID AS USERID,
       F.USERNAME,
       CASE
            WHEN G.PRIORITY = 1 THEN '[告警级别:低]'
            WHEN G.PRIORITY = 2 THEN '[告警级别:中]'
            WHEN G.PRIORITY = 3 THEN '[告警级别:高]' END
       ||B.NAME||'_'||A.IP||':'||G.NAME||'('||'#ACTUALVALUE#),产生时间:'||
       TO_CHAR(to_timestamp(NEW.CTIME/1000),'yyyy-MM-dd HH24:MI:SS') AS SMSCONTEXT,
       TO_CHAR(to_timestamp(NEW.CTIME/1000),'yyyy-MM-dd HH24:MI:SS') 
  FROM HQ_IP_VIEW A,
     HQ_SYSTEMGROUP_DETAIL_VIEW B,
     EAM_ROLE_RESOURCE_GROUP_MAP C,
     EAM_SUBJECT_ROLE_MAP D,
     EAM_SUBJECT E,
     IG.IGUSER F,
     EAM_ALERT_DEFINITION G
  WHERE A.PLATFORM_ID=B.PLID
     AND B.GROUPTYPE=13
     AND B.ID=C.RESOURCE_GROUP_ID
     AND C.ROLE_ID=D.ROLE_ID
     AND D.SUBJECT_ID = E.ID
     AND E.NAME = F.USERID
     AND G.RESOURCE_ID = A.RESOURCE_ID
     AND G.ID = NEW.ALERT_DEFINITION_ID;
  END IF;
  RETURN NEW;
  END;
 $$
 LANGUAGE plpgsql;

CREATE TRIGGER INSERT_SMS_QUEUE
  AFTER INSERT ON EAM_ALERT
  FOR EACH ROW EXECUTE PROCEDURE INSERT_SMS_QUEUE_F();
 

  
  ---------------新建MOCHA告警触发器 2012/11/19 ---------------
CREATE OR REPLACE FUNCTION INSERT_SMS_QUEUE_MOCHA_F() 
  RETURNS TRIGGER AS $$
DECLARE
  STATUS DECIMAL;
  BEGIN
  SELECT CVALUE INTO STATUS FROM IG.CODEDETAIL WHERE CCID = '153' AND CID = '5';
  IF STATUS = 1 AND NEW.FIXED = 0 THEN
  INSERT INTO SMS_QUEUE (QID,ALERTID,TEL,USERID,USERNAME,SMSCONTEXT,ALERTTIME)
  SELECT SMS_QUEUE_SEQ.nextval,
       NEW.ID,
       A.USERMOBILE AS TEL,
       A.USERID AS USERID,
       A.USERNAME,
       CASE
            WHEN substr(NEW.PRIORITY,1,1) = 0 THEN '[告警级别:未知]'
            WHEN substr(NEW.PRIORITY,1,1) = 1 THEN '[告警级别:信息]'
            WHEN substr(NEW.PRIORITY,1,1) = 2 THEN '[告警级别:警告]'
            WHEN substr(NEW.PRIORITY,1,1) = 3 THEN '[告警级别:次要]'
            WHEN substr(NEW.PRIORITY,1,1) = 4 THEN '[告警级别:严重]'
            WHEN substr(NEW.PRIORITY,1,1) = 5 THEN '[告警级别:致命]' 
       END
       ||substr(NEW.IP,2,LENGTH(NEW.IP)-2)||':'||substr(NEW.CONDITION,instr(NEW.CONDITION,'报警信息',1,1),
       instr(NEW.CONDITION,'产生时间',1,1)-instr(NEW.CONDITION,'报警信息',1,1))||','||substr(NEW.CONDITION,instr(NEW.CONDITION,'产生时间',1,1),21) AS SMSCONTEXT,
       NEW.CTIME
  FROM IG.IGUSER A,
       IG.ROLE B,
       IG.USERROLE C
  WHERE A.USERID=C.USERID
     AND B.ROLEID=C.ROLEID
     AND B.ROLETYPE='MOCHASMS';
  END IF;
  RETURN NEW;
  END;
 $$
 LANGUAGE plpgsql;
  
CREATE TRIGGER INSERT_SMS_QUEUE_MOCHA
  AFTER INSERT ON HQ_MOCHA_ALERT
  FOR EACH ROW EXECUTE PROCEDURE INSERT_SMS_QUEUE_MOCHA_F();